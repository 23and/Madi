CREATE TABLE MEMBER
(
	MEMBER_NUM			  INT(100) UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	EMAIL                 VARCHAR(500)  NOT NULL ,
	PW                    VARCHAR(500)  NOT NULL ,
	NAME                  VARCHAR(500)  NOT NULL ,
	PHONE                 VARCHAR(500)  NOT NULL ,
	TENDENCY              VARCHAR(500)  NULL  
)ENGINE=InnoDB DEFAULT CHARSET=UTF8;

CREATE TABLE MEMO
(
	MEMO_NUM                  INT(100) UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	MEMBER_NUM                INT(100) UNSIGNED NOT NULL,
	TITLE                 	  VARCHAR(500)  NULL ,
	CONTENT                   VARCHAR(500)  NULL ,
	HASHTAG1                  VARCHAR(500)  NULL ,
	HASHTAG2                  VARCHAR(500)  NULL ,
	HASHTAG3                  VARCHAR(500)  NULL ,
	BACK					  VARCHAR(500)  NULL ,
	WRITEDAY                  DATE  NOT NULL ,
	MUSIC_NUM                 INT(100) UNSIGNED NOT NULL
)ENGINE=InnoDB DEFAULT CHARSET=UTF8;

CREATE TABLE MUSIC
(
	MUSIC_NUM                 INT(100) UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
	TITLE               	  VARCHAR(500)  NOT NULL ,
	SINGER                 	  VARCHAR(500)  NOT NULL ,
	EMOTION					  VARCHAR(500)  NOT NULL ,
	URL	                      VARCHAR(500)  NULL
)ENGINE=InnoDB DEFAULT CHARSET=UTF8;

ALTER TABLE MEMO ADD FOREIGN KEY (MEMBER_NUM) REFERENCES MEMBER(MEMBER_NUM) ON DELETE CASCADE ON UPDATE CASCADE;

ALTER TABLE MEMO ADD FOREIGN KEY (MUSIC_NUM) REFERENCES MUSIC(MUSIC_NUM) ON DELETE CASCADE ON UPDATE CASCADE;	
	
